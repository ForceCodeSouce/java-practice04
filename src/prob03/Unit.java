package prob03;

public class Unit {

	private int Hp;
	private int Mana;

	public Unit(int hp, int mana) {
		super();
		Hp = hp;
		Mana = mana;
	}

	public int getHp() {
		return Hp;
	}

	public void setHp(int hp) {
		Hp = hp;
	}

	public int getMana() {
		return Mana;
	}

	public void setMana(int mana) {
		Mana = mana;
	}

}
